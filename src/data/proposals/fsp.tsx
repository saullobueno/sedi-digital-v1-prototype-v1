/** === Tipagem === */
export interface DataType {
  key: string;

  // Identificação FSP
  numeroFSP: string;
  createdAt: string;        // DD/MM/YYYY
  channel: string;          // Origem
  sediUnit: string;         // Unidade SEDI

  // Dados do cliente
  company: string;          // Nome fantasia
  corporateName: string;    // Razão Social
  billingCnpj: string;      // CNPJ faturamento (agora alfanumérico)
  branchCode: string;       // Filial/IBM/Sigla
  address: string;          // Endereço de execução do serviço

  // Contato
  contactName: string;
  department: string;
  email: string;
  phone: string;
  requestorName: string;
  requestEmail: string;

  // Serviço solicitado
  service: string;
  scope: string;
  stageMode: string;        // LP/LI/LO, Emissão, Renovação, Projeto etc.
  areaM2: number;
  cityUF: string;

  // Comercial
  deadline: string;         // Prazo desejado
  validityDays: number;     // Validade da proposta (dias)
  estimatedValue: string;   // Valor estimado
  priority: 'Alta' | 'Média' | 'Baixa';
  status: string[];         // Novo, Em análise, Enviado, Aprovado, Reprovado, Cancelado
  responsible: string;      // Responsável interno

  // Para linha expansível
  servicesSelected: string[]; // 1 a 5 itens
}

