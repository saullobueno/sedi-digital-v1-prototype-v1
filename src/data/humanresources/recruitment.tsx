import { TableProps, Tag } from "antd";

export interface DataType {
  key: string;
  candidate: string;
  email: string;
  phone?: string;
  position: string;
  department: string;
  seniority: "Júnior" | "Pleno" | "Sênior";
  modality: "Remoto" | "Híbrido" | "Presencial";
  contractType: "CLT" | "PJ" | "Estágio" | "Temporário";
  city: string;
  source: "LinkedIn" | "Indicação" | "Site da Empresa" | "Agência" | "Banco de Talentos";
  recruiter: string;
  salaryExpectation: number;
  nextInterview?: string;   // próxima etapa agendada
  stage: "Novo" | "Triagem" | "Entrevista Técnica" | "Entrevista RH" | "Case" | "Proposta" | "Onboarding";
  status: string[];         // ["Ativo" | "Aguardando" | "Aprovado" | "Reprovado" | "Desistiu" | "Stand-by"]
  createdAt: string;        // data de candidatura
  updatedAt: string;        // última atualização
}

export const columns: TableProps<DataType>["columns"] = [
  {
    title: "Candidato",
    key: "candidate",
    dataIndex: "candidate",
    render: (_, r) => (
      <div className="flex flex-col">
        <span className="font-medium">{r.candidate}</span>
        <span className="text-xs text-gray-500">{r.email}</span>
      </div>
    ),
  },
  {
    title: "Vaga",
    key: "position",
    dataIndex: "position",
    render: (_, r) => (
      <div className="flex flex-col">
        <span>{r.position}</span>
        <span className="text-xs text-gray-500">{r.department} • {r.seniority}</span>
      </div>
    ),
  },
  {
    title: "Modalidade",
    key: "modality",
    dataIndex: "modality",
  },
  {
    title: "Contrato",
    key: "contractType",
    dataIndex: "contractType",
  },
  {
    title: "Cidade",
    key: "city",
    dataIndex: "city",
  },
  {
    title: "Origem",
    key: "source",
    dataIndex: "source",
  },
  {
    title: "Recrutador",
    key: "recruiter",
    dataIndex: "recruiter",
  },
  {
    title: "Pretensão (R$)",
    key: "salaryExpectation",
    dataIndex: "salaryExpectation",
    render: (value) => <span>R$ {Number(value).toLocaleString("pt-BR", { minimumFractionDigits: 2 })}</span>,
  },
  {
    title: "Próx. Entrevista",
    key: "nextInterview",
    dataIndex: "nextInterview",
    render: (value) =>
      value ? <span>{new Date(value).toLocaleString("pt-BR")}</span> : <i className="text-gray-400">—</i>,
  },
  {
    title: "Etapa",
    key: "stage",
    dataIndex: "stage",
    render: (stage) => {
      const color =
        stage === "Novo" ? "blue" :
        stage === "Triagem" ? "geekblue" :
        stage === "Entrevista RH" ? "gold" :
        stage === "Entrevista Técnica" ? "purple" :
        stage === "Case" ? "cyan" :
        stage === "Proposta" ? "green" :
        "lime";
      return <Tag color={color}>{stage}</Tag>;
    },
  },
  {
    title: "Status",
    key: "status",
    dataIndex: "status",
    render: (_, { status }) => (
      <>
        {status.map((tag) => {
          let color: Parameters<typeof Tag>[0]["color"] = "blue";
          if (tag === "Ativo") color = "processing";
          if (tag === "Aguardando") color = "gold";
          if (tag === "Aprovado") color = "green";
          if (tag === "Reprovado") color = "red";
          if (tag === "Desistiu") color = "volcano";
          if (tag === "Stand-by") color = "default";
          return (
            <Tag color={color} key={tag}>
              {tag}
            </Tag>
          );
        })}
      </>
    ),
  },
  {
    title: "Criado em",
    key: "createdAt",
    dataIndex: "createdAt",
    render: (value) => <span>{new Date(value).toLocaleDateString("pt-BR")}</span>,
  },
  {
    title: "Atualizado em",
    key: "updatedAt",
    dataIndex: "updatedAt",
    render: (value) => <span>{new Date(value).toLocaleDateString("pt-BR")}</span>,
  },
];

